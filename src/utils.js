export function stakeContractForProvider(provider) {
  if (!provider || !provider.network) return;
  return new ethers.Contract(
    contracts.stake[provider.network.chainId],
    abiStake,
    provider.getSigner()
  );
}

export function tokenContractForProvider(provider) {
  if (!provider || !provider.network) return;
  return new ethers.Contract(
    contracts.token[provider.network.chainId],
    abiToken,
    provider.getSigner()
  );
}

